    private void jLabel14MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel14MouseClicked
        // TODO add your handling code here:
        double cantidad = Double.parseDouble(TxtCantidad.getText());
        double unitario = Double.parseDouble(TxtUnitario.getText());
        double multi = cantidad * unitario;
        String resultado = Double.toString(multi);
        TxtTotal.setText(resultado);
    }//GEN-LAST:event_jLabel14MouseClicked

    private void jLabel13MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel13MouseClicked
        // TODO add your handling code here:
        double total = Double.parseDouble(TxtTotal.getText());
        double Efectivo = Double.parseDouble(TxtEfectivo.getText());
        double vuelto = Efectivo - total;
        String Final = Double.toString(vuelto);
        TxtVuelto.setText("S/."+Final);
        
    }//GEN-LAST:event_jLabel13MouseClicked

    private void jLabel16MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel16MouseClicked
        // TODO add your handling code here:
        LocalDateTime fechaHoraActual = LocalDateTime.now();

        // Formatear la fecha y hora
        DateTimeFormatter formateador = DateTimeFormatter.ofPattern("yyyy-MM-dd"+"      "+"HH:mm:ss");
        String fechaHoraFormateada = fechaHoraActual.format(formateador);
        TxtFecha.setText(fechaHoraFormateada);
    }//GEN-LAST:event_jLabel16MouseClicked

    private int suma(int cantidad ){
        cantidad = cantidad + 1;
        return cantidad;
    }

